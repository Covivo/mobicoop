namespace App\Article\Entity;

/**
* An iframe
* @author Maxime Bardot <maxime.bardot@mobicoop.org>
*/
class Iframe
{
    /**
     * @var string title of the iframe
     */
    private $title;

    /**
     * @var string Src of the iframe
     */
    private $src;

    /**
     * @var int Width of the iframe
     */
    private $width;

    /**
     * @var int Height of the iframe
     */
    private $height;

    /**
     * @var string Allow field of the iframe
     */
    private $allow;

    public function getTitle(): ?string
    {
        return $this->title;
    }

    public function setTitle(string $title): self
    {
        $this->title = $title;

        return $this;
    }

    public function getSrc(): ?string
    {
        return $this->src;
    }

    public function setSrc(string $src): self
    {
        $this->src = $src;

        return $this;
    }

    public function getWidth(): ?int
    {
        return $this->width;
    }

    public function setWidth(int $width): self
    {
        $this->width = $width;

        return $this;
    }

    public function getHeight(): ?int
    {
        return $this->height;
    }

    public function setHeight(int $height): self
    {
        $this->height = $height;

        return $this;
    }

    public function getAllow(): ?string
    {
        return $this->allow;
    }

    public function setAllow(string $allow): self
    {
        $this->allow = $allow;

        return $this;
    }
}
