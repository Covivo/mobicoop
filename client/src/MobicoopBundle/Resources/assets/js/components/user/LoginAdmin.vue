<template>
  <v-container
    fluid
    style="height: 100%"
  >
    <v-row
      justify="center"
    >
      <v-col
        cols="12"
        md="8"
        xl="6"
        align="center"
      >
        <h1>{{ $t('title', {user: userEmail}) }}</h1>
      </v-col>
    </v-row>
    <div class="pt-12 mt-12">
      <v-row
        justify="center"
        align="center"
        class="text-center"
      >
        <v-col class="col-4">
          <v-alert
            v-if="errorDisplay!==''"
            type="error"
            class="text-left"
          >
            {{ errorDisplay }}
          </v-alert>
          <v-form
            ref="form"
            v-model="valid"
            lazy-validation
            :action="action"
            method="POST"
          >
            <v-text-field
              id="email"
              v-model="userEmail"
              :rules="userRules"
              :label="$t('userEmail')"
              name="emailDelegate"
              required
            />
            <v-text-field
              v-model="adminEmail"
              :rules="adminRules"
              :label="$t('adminEmail')"
              name="email"
              required
            />

            <v-text-field
              id="password"
              v-model="password"
              :append-icon="show1 ? 'mdi-eye' : 'mdi-eye-off'"
              :rules="passwordRules"
              :type="show1 ? 'text' : 'password'"
              name="password"
              :label="$t('password')"
              @click:append="show1 = !show1"
            />

            <v-btn
              :disabled="!valid"
              :loading="loading"
              color="secondary"
              type="submit"
              rounded
              @click="validate"
            >
              {{ $t('connection') }}
            </v-btn>
          </v-form>
        </v-col>
      </v-row>
    </div>
  </v-container>
</template>
<script>
import axios from "axios";
import { merge } from "lodash";
import {messages_en, messages_fr} from "@translations/components/user/LoginAdmin/";

export default {
  i18n: {
    messages: {
      'en': messages_en,
      'fr': messages_fr
    }
  },
  props: {
    email: {
      type: String,
      default: null
    },
    delegateEmail: {
      type: String,
      default: null
    },
    errorMessage: {
      type: Object,
      default: null
    },
  },
  data() {
    return {
      valid: true,
      loading: false,
      show1: false,
      errorDisplay: "",
      userEmail: this.delegateEmail,
      userRules: [
        v => !!v || this.$t("userEmailRequired"),
        v => /.+@.+/.test(v) || this.$t("userEmailInvalid")
      ],
      adminEmail: this.email,
      adminRules: [
        v => !!v || this.$t("adminEmailRequired"),
        v => /.+@.+/.test(v) || this.$t("adminEmailInvalid")
      ],
      password: "",
      passwordRules: [
        v => !!v || this.$t("passwordRequired")
      ]
    };
  },
  methods: {
    validate() {
      if (this.$refs.form.validate()) {
        this.loading = true;
      }
    },
    treatErrorMessage(errorMessage) {
      this.errorDisplay = this.$t(errorMessage.value);
      this.loading = false;
    }
  }
};
</script>